// Bundles the seed recipe corpus into the client.
//
// The recipe-suggestions popup samples from a small pool of recipes client-side
// so it works instantly and even when the live recipe-search backend is offline.
// The backend's runtime artifact (server/data/recipes.ndjson) already carries the
// only fields the client needs: id, title, and the ingredient node indices. This
// script projects that file down to those three fields and emits a typed module.
//
// Run with:  npm run prep:recipes
// Output (src/recipePool.ts) is committed; CI does NOT run this.

import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const SRC = join(ROOT, 'server', 'data', 'recipes.ndjson');
const OUT = join(ROOT, 'src', 'recipePool.ts');

const raw = await readFile(SRC, 'utf8');
const pool = raw
  .split('\n')
  .map((line) => line.trim())
  .filter(Boolean)
  .map((line) => {
    const { id, title, nodes } = JSON.parse(line);
    return { id, title, nodes };
  });

const banner =
  '// GENERATED by scripts/build-recipe-pool.mjs from server/data/recipes.ndjson.\n' +
  '// Do not edit by hand; run `npm run prep:recipes` to regenerate.\n';
const body =
  'export interface PoolRecipe {\n' +
  '  id: string;\n' +
  '  title: string;\n' +
  '  nodes: number[];\n' +
  '}\n\n' +
  `export const RECIPE_POOL: PoolRecipe[] = ${JSON.stringify(pool, null, 2)};\n`;

await writeFile(OUT, banner + '\n' + body);
console.log(`Wrote ${pool.length} recipes to ${OUT}`);
